////1014. Даны вещественные положительные числа a, b, c, d. Выясните, может ли прямоугольник со сторонами a, b 
//уместиться внутри прямоугольника со сторонами c, d так, чтобы каждая сторона внутреннего прямоугольника была
//параллельна или перпендикулярна стороне внешнего прямоугольника.
function canFit(a, b, c, d) {
    // Проверяем первую ориентацию
    const fitsFirstOrientation = (a <= c && b <= d);
    // Проверяем вторую ориентацию
    const fitsSecondOrientation = (a <= d && b <= c);

    // Возвращаем true, если хотя бы одно из условий выполняется
    return fitsFirstOrientation || fitsSecondOrientation;
}

// Пример использования:
const a = 3; // длина первой стороны внутреннего прямоугольника
const b = 4; // длина второй стороны внутреннего прямоугольника
const c = 5; // длина первой стороны внешнего прямоугольника
const d = 6; // длина второй стороны внешнего прямоугольника

const result = canFit(a, b, c, d);
console.log(result ? "Прямоугольник помещается." : "Прямоугольник не помещается.");